    class HuanYuan extends Thread {

        String filePath = null;
        InputStream is = null;
        OutputStream os = null;

        public HuanYuan(String filePath) {
            this.filePath = filePath;
        }

        public void run() {
            File file = null;
            String fileName = filePath.substring(0, filePath.lastIndexOf("-"));//获取真实的文件名
            if (new File(fileName).exists()) {
                System.out.println("文件存在！不进行还原");
                return;
            }
            try {
                os = new FileOutputStream(fileName, true);

                System.out.println(fileName);
                int i = 1;
                byte[] data = new byte[1024 * 1024];
                while (true) {
                    file = new File(fileName + "-" + i + ".jy1091part");
                    if (file.exists()) {
                        System.out.println("文件存在");
                    } else {
                        System.out.println(file.getPath() + "文件不存在");
                    }
                    if (!file.exists()) {//如果文件不存在
                        return;
                    }
                    is = new FileInputStream(file);
                    long yuShu = file.length() % (1024 * 1024);//余数
                    long duoS = file.length() / 1024 / 1024;
                    if (yuShu != 0) {
                        duoS++;
                    }
                    int count = 0;
                    while (is.read(data, 0, data.length) != -1) {
                        count++;

                        os.write(data, 0, data.length);
                        if (yuShu != 0 && duoS == count - 1) {
                            data = new byte[Integer.parseInt(yuShu + "")];
                        }
                    }
                    count = 0;
                    is.close();

                    i++;
                }

            } catch (Exception ex) {
                ex.printStackTrace();
            } finally {
                try {
                    JOptionPane.showMessageDialog(miangui, "还原成功!");
                    if (is != null) {
                        is.close();
                    }
                    if (os != null) {
                        os.close();
                    }
                } catch (IOException ex) {
                    Logger.getLogger(PartFile_GUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }
    }
