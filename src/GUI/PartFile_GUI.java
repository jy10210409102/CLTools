    class FenGe extends Thread {

        String partFile = null;//需分割的文件路径
        String saveFile = null;//保存的路径
        long partSize;//分割文件的最小单元
        InputStream is = null;
        OutputStream os = null;

        public FenGe(String partFile, String saveFile, long partSize) {
            this.partFile = partFile;
            this.saveFile = saveFile;
            this.partSize = partSize;
        }

        public void run() {
            try {
                is = new FileInputStream(this.partFile);
                long n = 0;//记录总共读取的字节数
                int i = 1;//计数器
                byte[] data = new byte[1024 * 1024];
                long fileSize = new File(this.partFile).length();
                long count = fileSize / partSize;//分成多少部分
                int yuShu = Integer.parseInt(fileSize % partSize + "");
                System.out.println("文件大小：" + fileSize + "余数：" + yuShu);
                if (yuShu != 0) {
                    count++;
                }
                os = new FileOutputStream(this.saveFile + "\\" + (new File(this.partFile).getName() + "-" + i + ".jy1091part"));
                while (n < fileSize) {
                    if (n >= partSize && n % partSize == 0) {
                        i++;//part加一
                        System.out.println(" i:" + i + "  count:" + count);
                        os.close();
                        os = new FileOutputStream(this.saveFile + "\\" + (new File(this.partFile).getName() + "-" + i + ".jy1091part"));
                    }
                    if (i == count) {
                        System.out.println("执行到：" + count + " i:" + i);
                        data = new byte[yuShu];
                    }

                    n = n + is.read(data, 0, data.length);
                    System.out.println("读取的字节数：" + Tool.Tool().formatFileSize(n));
                    os.write(data, 0, data.length);
                }
                is.close();//关闭
                os.close();
                JOptionPane.showMessageDialog(miangui, "分割成功!");
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }

    
