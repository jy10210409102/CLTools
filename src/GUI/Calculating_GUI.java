    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String str1 = jTextField2.getText().replace("0x", "");
        String str2 = null;//第二个实际参数
        int index = jComboBox2.getSelectedIndex();//获得bit几
        int Nount = Integer.parseInt(jTextField4.getText());//只能为1 或0最好
        if (chuShuscope == HANDLEINPUT) {
            str2 = jTextField3.getText().replace("0x", "");
        } else if (chuShuscope == BIT) {
            str2 = (((int) Tool.BITMap.get(index)) - Nount) + "";
        }
        int operator = jComboBox1.getSelectedIndex();
        switch (operator) {
            case yu:
                yu(str1, str2);
                break;
            case huo:
                huo(str1, str2);
                break;
            case yiHuo:
                yiHuo(str1, str2);
                break;
            case zuoYi:
                zuoYi(str1, str2);
                break;
            case youYi:
                youYi(str1, str2);
                break;
        }
        jTextArea1.setCaretPosition(jTextArea1.getText().length());
    }//GEN-LAST:event_jButton2ActionPerformed

