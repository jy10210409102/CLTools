    //二进制计算过程
    public void outCourse(String str1, String str2) {
        String tran2_1 = strTran2(str1);
        String tran2_2 = strTran2(str2);
        if (tran2_1.length() >= tran2_2.length()) {
            int cout = tran2_1.length() - tran2_2.length();
            for (int i = 0; i < cout; i++) {
                tran2_2 = "0" + tran2_2;
            }
        } else {
            int cout = tran2_2.length() - tran2_1.length();
            for (int i = 0; i < cout; i++) {
                tran2_1 = "0" + tran2_1;
            }
        }
        outTxt("  " + tran2_1 + "\r\n" + "  " + tran2_2 + "\r\n=");
    }

