    //异或
    public Integer XOR(String str1, String str2) {

        return strTran16(str1) ^ strTran16(str2);
    }

    //与运算
    public Integer YU(String str1, String str2) {
        return strTran16(str1) & strTran16(str2);
    }

    //或运算
    public Integer HUO(String str1, String str2) {
        return strTran16(str1) | strTran16(str2);
    }

    //左移运算
    public Integer ZUOYI(String str1, String str2) {
        return strTran16(str1) << Integer.parseInt(str2);
    }

    //右移运算
    public Integer YOUYI(String str1, String str2) {
        return strTran16(str1) >> Integer.parseInt(str2);
    }

